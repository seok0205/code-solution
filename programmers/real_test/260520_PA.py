'''
채팅 비속어 필터링

문제 설명

당신은 게임 채팅창의 비속어를 감지하여 마스킹(#) 처리하는 필터링 시스템을 개발하려고 합니다.
사용자들은 비속어 제재를 피하고자 비속어의 일부 글자를 마스킹 문자인 점(.)으로 가려서 입력합니다.
점(.) 문자는 최소 0글자에서 최대 $k$글자까지의 임의의 문자로 대체될 수 있습니다.
비속어 사전 dic, 대체 가능한 최대 글자 수 k, 그리고 사용자가 입력한 채팅 문자열 chat이 주어질 때, 비속어로 판단되는 단어를 모두 해당 단어의 길이만큼 #으로 변환한 문자열을 반환하는 함수를 작성해 주세요.

필터링 규칙

1. 단어의 기준: 채팅 문자열 chat은 공백( )을 기준으로 단어를 구분합니다.
2. 정확한 단어 일치: 비속어 사전에 있는 단어와 정확히 일치하는 단어 뭉치만 필터링합니다.
사전에 abcd가 있을 때, abcd는 필터링 대상입니다.사전에 abcd가 있을 때, abcde는 다른 단어이므로 필터링하지 않습니다.
3. 와일드카드 (.) 규칙: 사용자가 입력한 단어에 포함된 점(.)은 0글자 이상 $k$글자 이하의 임의의 문자로 치환되어 사전에 있는 비속어를 만들 수 있다면 비속어로 판단합니다.

    예시 1
    (k=2, 사전 ['abcd']일 때): 단어 .bcd는 . 자리에 a(1글자)를 넣으면 abcd를 만들 수 있으므로 비속어입니다. (####로 변환)단어 .c.는 앞의 .에 ab(2글자), 뒤의 .에 d(1글자)를 넣으면 abcd를 만들 수 있으므로 비속어입니다. (###로 변환)

    예시 2
    (k=2, 사전 ['abcd']일 때): 단어 .d는 . 자리에 abc(3글자)가 들어가야 abcd가 됩니다. 하지만 $k=2$이므로 3글자를 채울 수 없어 비속어가 아닙니다. (.d 그대로 유지)
'''

import re

def solution(k, dic, chat):
    answer = []
    
    # 1. 채팅을 공백 기준으로 단어별로 분리
    words = chat.split()
    
    for word in words:
        is_bad = False
        
        # 2. 채팅 단어의 '.'을 정규표현식의 공백 허용 범위인 '.{0,k}'로 치환
        # 정규식의 시작(^)과 끝($)을 명시하여 '정확히 한 단어'로만 매칭되도록 제한 (abcde 방지)
        word_pattern = '^' + word.replace('.', f'.{{0,{k}}}') + '$'
        
        # 3. 비속어 사전에 있는 단어들과 매칭되는지 확인
        for bad_word in dic:
            if re.match(word_pattern, bad_word):
                is_bad = True
                break  # 하나라도 매칭되면 더 볼 필요 없이 탈출
                
        # 4. 비속어라면 기존 단어의 길이만큼 '#'으로 변환, 아니면 그대로 유지
        if is_bad:
            answer.append('#' * len(word))
        else:
            answer.append(word)
            
    # 5. 변환된 단어들을 다시 공백으로 합쳐서 반환
    return ' '.join(answer)